CREATE TABLE IF NOT EXISTS datosMasivos (
    ID INT(11) NOT NULL AUTO_INCREMENT PRIMARY KEY,
    CTO VARCHAR(255),
    BORNE INT(11),
    LINE_ID_INICIAL VARCHAR(255),
    VNO_CODE_INICIAL VARCHAR(20),
    OLT VARCHAR(255),
    SLOT INT(11),
    PORT INT(11),
    ONU_INICIAL INT(11),
    ONU_FINAL INT(11),
    ESTADO VARCHAR(20),
    POTENCIA_ANTES DECIMAL(10,2),
    POTENCIA_DESPUES DECIMAL(10,2),
    POTENCIA_CAMPO DECIMAL(10,2),
    LINE_ID_FINAL VARCHAR(255),
    VNO_CODE_FINAL VARCHAR(10),
    COMENTARIO VARCHAR(100),
    GRUPO VARCHAR(255),
    FECHA DATE,
    HORA_INICIO TIME,
    HORA_CIERRE TIME,
    GESTOR VARCHAR(255),
    VALIDACION VARCHAR(255),
    VALIDACION_2 VARCHAR(255),
    ESTADO_EN_OSP VARCHAR(255),
    UBICAR_EN_CAMPO VARCHAR(255),
    CTO_DONDE_SE_DEJA VARCHAR(255),
    BORNE_DSDEJA VARCHAR(30)
);